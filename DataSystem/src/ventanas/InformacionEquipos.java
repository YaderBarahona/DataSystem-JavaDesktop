    public InformacionEquipos() {
        initComponents();
        
        setSize(670, 530);
        this.setLocationRelativeTo(null);
        this.setResizable(false);
      
        IDEquipo = InformacionCliente.ID_equipo;

        user = Login.user;
        IDCliente_update = GestionarClientes.IDCliente_update;

        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        this.setLocationRelativeTo(null);
        ImageIcon wallpaper = new ImageIcon("src/images/wallpaperPrincipal.jpg");

        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(label_wallpaper.getWidth(), label_wallpaper.getHeight(),
                Image.SCALE_DEFAULT));

        label_wallpaper.setIcon(icono);
        this.repaint();
        
         try
        {
            Connection cn = Conexion.db();
            //instruccion hacia la db para indicar cual id estamos consultando
            PreparedStatement pst = cn.prepareStatement("select nombre_cliente from clientes where id_cliente = '" + IDCliente_update + "'");

            ResultSet rs = pst.executeQuery();

            if (rs.next())
            {
                nom_cliente = rs.getString("nombre_cliente");
                

            }

            cn.close();

        } catch (SQLException e)
        {
            System.err.println("Error en cargar cliente" + e);
            JOptionPane.showMessageDialog(null, "Error al cargar, contacte al administrador");
        }
        
        txt_nombre.setText(nom_cliente);
         setTitle("Equipo del cliente " + nom_cliente);
         
          try
        {
            Connection cn2 = Conexion.db();
            PreparedStatement pst = cn2.prepareStatement("select * from equipos where id_equipo = '" + IDEquipo + "'");

            ResultSet rs= pst.executeQuery();

            if (rs.next())
            {

                //a cada txt le ponemos el nombre y demas propiedades del registro que haya seleccionado de la tabla esto por el metodo setText y llamando al objeto rs con el metodo getString (campo o columna que se vaya a recuperar de la db)
                txt_modelo.setText(rs.getString("modelo"));
                txt_numero_serie.setText(rs.getString("num_serie"));
                txt_ultima_modificacion.setText(rs.getString("ultima_modificacion"));

                combobox_tipo_equipo.setSelectedItem(rs.getString("tipo_equipo"));
                combobox_marca.setSelectedItem(rs.getString("marca"));
                combobox_estatus.setSelectedItem(rs.getString("estatus"));
                
                String dia = "", mes = "", annio = "";
                
                //valores para la fecha tomados desde la db ya que esta por dia mes y año
                dia = rs.getString("dia_ingreso");
                mes = rs.getString("mes_ingreso");
                annio = rs.getString("annio_ingreso");
                
                txt_fecha.setText(dia +" del "+ mes +" del " +annio);
                
                jTextPane_daños.setText(rs.getString("Observaciones"));
                jTextPane_comentarios.setText(rs.getString("comentarios_tecnicos"));
                
                label_comentarios.setText("Comentarios y actualizacion del técnico "+rs.getString("revision_tecnica_de"));
               
            }

            cn2.close();

        } catch (SQLException e)
        {
            System.err.println("Error en cargar usuario" + e);
            JOptionPane.showMessageDialog(null, "Error al cargar, contacte al administrador");
        }
    }
        
    
    
