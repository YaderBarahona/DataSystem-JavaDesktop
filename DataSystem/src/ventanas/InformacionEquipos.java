    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int validacion = 0;
        
        String nombre, modelo, numero_serie, ultima_modificacion, fecha_ingreso, observaciones;
        
        String combo_tipo_equipo, combo_marca, combo_estatus;
       
        
        //recuperamos u obtenemos el valor de los txt mediante las variables declaradas para cada uno  
        nombre = txt_nombre.getText().trim();
        modelo = txt_modelo.getText().trim();
        numero_serie = txt_numero_serie.getText().trim();
        ultima_modificacion = txt_ultima_modificacion.getText().trim();
        fecha_ingreso = txt_fecha.getText().trim();
        
        //recuperamos el valor de los textpane
        observaciones = jTextPane_daños.getText();
        
        //recuperamos el valor de la selección de los combobox
        combo_tipo_equipo = combobox_tipo_equipo.getSelectedItem().toString();
        combo_marca = combobox_marca.getSelectedItem().toString();
        combo_estatus = combobox_estatus.getSelectedItem().toString();
      
        //validamos si los campos estan vacios        
         if (modelo.equals(""))
        {
         txt_modelo.setBackground(Color.red);
         validacion++;
        }
         
         if (numero_serie.equals(""))
        {
            txt_numero_serie.setBackground(Color.red);
            validacion++;
        }
          
          if (observaciones.equals(""))
        {
            jTextPane_daños.setText("Sin observaciones");
            validacion++;
        }
       
          
          if (validacion==0)
        {
            try
            {
                
                Connection cn  =Conexion.db();
               
                PreparedStatement pst = cn.prepareStatement("update equipos set tipo_equipo = ?, marca = ?, modelo = ?, num_serie = ?, Observaciones = ?, estatus = ? , ultima_modificacion = ? where id_equipo = "+IDEquipo);
                
                pst.setString(1, combo_tipo_equipo);
                pst.setString(2, combo_marca);
                pst.setString(3, modelo);
                pst.setString(4, numero_serie);
                pst.setString(5, observaciones);
                pst.setString(6, combo_estatus);
                pst.setString(7, user);
                
                pst.executeUpdate();
                cn.close();
                
                Limpiar();
                
                txt_nombre.setBackground(Color.green);
                txt_modelo.setBackground(Color.green);
                txt_numero_serie.setBackground(Color.green);
                txt_fecha.setBackground(Color.green);
                
                txt_ultima_modificacion.setText(user);
                
                jTextPane_daños.setBackground(Color.green);
                jTextPane_comentarios.setBackground(Color.green);
                
                JOptionPane.showMessageDialog(null, "Equipo actualizado con éxito");
                
                this.dispose();
                
            } catch (HeadlessException | SQLException e)
            {
                System.err.println("Error en la instruccion actualizar");
            }
            
        }
          
    }//GEN-LAST:event_jButton1ActionPerformed

